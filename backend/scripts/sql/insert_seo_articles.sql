### 2. Conectivos são Essenciais
Use conectivos para garantir a coesão:
- Adição: Além disso, ademais, outrossim
- Contraste: Entretanto, todavia, não obstante
- Conclusão: Portanto, destarte, por conseguinte
- Explicação: Isso porque, uma vez que, haja vista

### 3. Evite Erros Comuns
- NÃO use gírias ou linguagem coloquial
- NÃO faça cópia dos textos motivadores
- NÃO esqueça dos 5 elementos da proposta
- NÃO fuja do tema proposto

## Quantas Linhas Escrever?

A redação do ENEM permite de 7 a 30 linhas. Para nota máxima:
- **Mínimo recomendado**: 25 linhas
- **Ideal**: 28-30 linhas
- Distribua: 5-7 (intro) + 8 (D1) + 8 (D2) + 7 (conclusão)

