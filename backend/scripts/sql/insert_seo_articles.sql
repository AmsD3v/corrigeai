## Estrutura Ideal da Redação

### Introdução (5-7 linhas)
A introdução deve conter:
1. **Contextualização**: Apresente o tema usando uma referência (dado, citação, fato histórico)
2. **Tese**: Deixe claro seu posicionamento sobre o problema
3. **Prévia dos argumentos**: Sinalize brevemente os dois argumentos principais

**Exemplo de estrutura:**
> [Repertório sociocultural], evidencia-se que [contextualização do tema]. Nesse cenário, [apresentação da problemática] configura-se como um desafio, uma vez que [argumento 1] e [argumento 2].

### Desenvolvimento 1 (7-9 linhas)
- Retome o primeiro argumento da introdução
- Apresente um repertório legítimo (dado, autor, obra)
- Desenvolva a argumentação com análise crítica
- Conecte ao tema da redação

### Desenvolvimento 2 (7-9 linhas)
- Retome o segundo argumento
- Use outro repertório diferente
- Mantenha coerência com a tese apresentada
- Prepare terreno para a conclusão

### Conclusão com Proposta de Intervenção (7-9 linhas)
A conclusão deve conter **obrigatoriamente** os 5 elementos:
1. **Agente**: Quem vai agir (Governo, escolas, mídia, sociedade)
2. **Ação**: O que será feito
3. **Meio**: Como será feito
4. **Finalidade**: Objetivo da ação
5. **Detalhamento**: Especificação de algum elemento

